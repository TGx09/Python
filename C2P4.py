# Write a Python script to check whether given year is leap year or not. (use nested if...else)

year = int(input("Enter year:\t"))

if year % 4 == 0:
    if year % 100 == 0:
        if year % 400 == 0:
            print(year, "is a leap year")
        else:
            print(year, "is not a leap year")
    else:
        print(year, "is a leap year")
else:
    print(year, "is not a leap year")
