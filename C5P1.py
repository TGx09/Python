import math

print("Pi:")
print(f"{math.pi: 2f}")
print()

print("Factorial:")
print("Factorial:\t", math.factorial(
    int(input("Enter number to find its factorial:\t"))))
print()

print("Root:")
print("Root:\t", math.sqrt(int(input("Enter number to find its root:\t"))))
print()

print("Pow:")
print("Pow:\t", math.pow(int(input("Enter first number:\t")),
      (int(input("Enter second number:\t")))))
print()

print("Sin:")
print("Sin:\t", math.sin(int(input("Enter number to find its Sin:\t"))))
print()
